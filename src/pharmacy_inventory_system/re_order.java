public void vendor(){
    try {
            String name = companyNameSelection.getSelectedItem().toString();
            DB_Con db = new DB_Con();
            Connection c = (Connection) db.getConnect();
            String q="SELECT * FROM Vendor ";
            PreparedStatement s = (PreparedStatement) c.prepareStatement(q);
            ResultSet rs = (ResultSet) s.executeQuery();
            companyNameSelection.removeAllItems();
            while (rs.next()) {
                companyNameSelection.addItem(rs.getString("Name"));
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
}
