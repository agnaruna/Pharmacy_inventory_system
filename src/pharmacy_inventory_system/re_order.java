    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed
        try {
            DefaultTableModel dt = (DefaultTableModel) records_tbl.getModel();
            SimpleDateFormat d = new SimpleDateFormat("yyyy-MM-dd");
            int rc = dt.getRowCount();
            for (int i = 0; i < rc; i++) {
                String thedate = d.format(date.getDate());
                String oid = dt.getValueAt(i, 0).toString();
                String cname = dt.getValueAt(i, 1).toString();
                String dname = dt.getValueAt(i, 2).toString();
                String qty = dt.getValueAt(i, 3).toString();
                String un_price = dt.getValueAt(i, 4).toString();
                String tot_price = dt.getValueAt(i, 5).toString();
                DB_Con db = new DB_Con();
                java.sql.Connection c = db.getConnect();
                Statement s = (Statement) c.createStatement();
                s.executeUpdate(" INSERT INTO order (orderId, companyName, drugName, quantity, Unitprice, TotalPrice, Date) VALUES ('" + oid + "','" + cname + "','" + dname + "','" + qty + "','" + un_price + "','" + tot_price + "','" + thedate + "') ");
                JOptionPane.showMessageDialog(this, "success");
            }
            JOptionPane.showMessageDialog(null, "Data Seved");
        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
        }       // TODO add your handling code here:
    }//GEN-LAST:event_payActionPerformed

